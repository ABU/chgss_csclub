#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;

    cout << "a + b = " << (a + b) << endl;  // 加法
    cout << "a - b = " << (a - b) << endl;  // 減法
    cout << "a * b = " << (a * b) << endl;  // 乘法
    cout << "a / b = " << (a / b) << endl;  // 除法 (整數)
    cout << "a % b = " << (a % b) << endl;  // 餘數

    return 0;
}
