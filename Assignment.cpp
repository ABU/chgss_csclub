#include <iostream>
using namespace std;

int main() {
    int x = 5;
    x += 3;  // x = x + 3
    cout << "x = " << x << endl;

    x -= 2;  // x = x - 2
    cout << "x = " << x << endl;

    x *= 4;  // x = x * 4
    cout << "x = " << x << endl;

    x /= 3;  // x = x / 3
    cout << "x = " << x << endl;

    x %= 5;  // x = x % 5
    cout << "x = " << x << endl;

    return 0;
}

